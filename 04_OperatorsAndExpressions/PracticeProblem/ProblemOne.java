// Write a program to calculate the area of a rectangle using int variables and the * operator.

public class ProblemOne {

    public static void main(String[] args) {
        int length = 10, breath = 20;
        System.out.println("The length and breath of the rectangle is: " + length + ", " + breath);
        int area = length * breath;
        System.out.println("The area of the rectangle is: " + area);
    }
};
